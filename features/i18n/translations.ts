export type TranslationKey = keyof (typeof translations)["pt-BR"];

export const translate = (
  language: AppLanguagePreference,
  key: TranslationKey,
  params: TranslationParams = {},
) => {
  const entry = translations[language][key] ?? translations["pt-BR"][key];
  return typeof entry === "function" ? entry(params) : entry;
};

export const translateDay = (
  language: AppLanguagePreference,
  day: string,
) => (language === "en" ? enDays[day] : ptDays[day]) ?? day;

export const translateDayAbbrev = (
  language: AppLanguagePreference,
  day: string,
) => (language === "en" ? enDayAbbrev[day] : ptDayAbbrev[day]) ?? day;
